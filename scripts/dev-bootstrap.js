#!/usr/bin/env node
'use strict';

// Prepares a turnkey docker-compose demo:
//   node scripts/dev-bootstrap.js
//
// Generates an Ed25519 license-signing key pair and writes a complete .env
// (next to docker-compose.yml) with the keys + demo defaults. The private key
// goes to blueeye-licens (LICENSE_SIGNING_KEY) and the public key to
// blueeye-server (LICENSE_PUBLIC_KEY) — both base64-of-PEM. Existing .env is not
// overwritten.

const crypto = require('crypto');
const fs = require('fs');
const path = require('path');

const ENV_PATH = path.join(__dirname, '..', '.env');

if (fs.existsSync(ENV_PATH)) {
  console.error(`${ENV_PATH} already exists — refusing to overwrite. Delete it to regenerate.`);
  process.exit(1);
}

const { publicKey, privateKey } = crypto.generateKeyPairSync('ed25519');
const privB64 = Buffer.from(privateKey.export({ type: 'pkcs8', format: 'pem' })).toString('base64');
const pubB64 = Buffer.from(publicKey.export({ type: 'spki', format: 'pem' })).toString('base64');
const rand = (n = 24) => crypto.randomBytes(n).toString('base64url');

const env = `# Generated by scripts/dev-bootstrap.js — local demo only. Do NOT commit.
# MySQL
MYSQL_ROOT_PASSWORD=${rand(12)}
DB_USER=blueeye
DB_PASSWORD=${rand(12)}

# Staff admin (seeded into both server and licens)
ADMIN_EMAIL=admin@blueeye.local
ADMIN_PASSWORD=${rand(12)}

# JWT secrets (separate per service)
SERVER_JWT_SECRET=${rand(48)}
LICENS_JWT_SECRET=${rand(48)}

# License signing key pair (base64 of PEM). Private -> licens, public -> server.
LICENSE_SIGNING_KEY=${privB64}
LICENSE_PUBLIC_KEY=${pubB64}

# Demo license + identifiers (the demo license is seeded into licens with this key)
LICENSE_KEY=DEMO-DEMO-DEMO-DEMO-DEMO
LICENSE_SERVER_ID=gnf-server-1
SEED_DEMO_LICENSE_KEY=DEMO-DEMO-DEMO-DEMO-DEMO
SEED_DEMO_MAX_AGENTS=100

# Demo agent enrollment (seeded into server; the agent uses this code once)
SEED_DEMO=1
SEED_DEMO_ENROLLMENT_CODE=DEMO-ENROLL-CODE
AGENT_ENROLLMENT_CODE=DEMO-ENROLL-CODE
`;

fs.writeFileSync(ENV_PATH, env, { mode: 0o600 });
console.info(`Wrote ${ENV_PATH} with a fresh Ed25519 key pair and demo defaults.`);
console.info('Next: docker compose up --build');
